        String pass = jTFPass.getText();
        
        if (usuario.isEmpty() || pass.isEmpty()){
            JOptionPane.showMessageDialog(this, "No ha ingresado los datos solicitados");
        } else if (usuario.equals("alumno@ulp.edu.ar") && pass.equals("12345678")) {
            JOptionPane.showMessageDialog(this, "Datos correctos!!! Bienvenido!!!");
        } else {
            JOptionPane.showMessageDialog(this, "Datos Incorrectos");
        }
        
        
